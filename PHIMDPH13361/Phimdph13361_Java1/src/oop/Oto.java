package oop;

public class Oto {
    private String MaOto;
    private String HangOto;
    private String DongCo;
    private String TenNhienLieu;
    private int  GiaTien;

    public String getMaOto() {
        return MaOto;
    }

    public void setMaOto(String MaOto) {
        this.MaOto = MaOto;
    }

    public String getHangOto() {
        return HangOto;
    }

    public void setHangOto(String HangOto) {
        this.HangOto = HangOto;
    }

    public String getDongCo() {
        return DongCo;
    }

    public void setDongCo(String DongCo) {
        this.DongCo = DongCo;
    }

    public String getTenNhienLieu() {
        return TenNhienLieu;
    }

    public void setTenNhienLieu(String TenNhienLieu) {
        this.TenNhienLieu = TenNhienLieu;
    }

    public int getGiaTien() {
        return GiaTien;
    }

    public void setGiaTien(int GiaTien) {
        this.GiaTien = GiaTien;
    }
   
}
